function constructPolygons(mapToDrawOn)
{    
     console.log("constructPolygons function called");
     //console.log("polygonRegions is: ", polygonRegions);
     var polygonRegionsLength = polygonRegions.length;
     console.log("polygonRegionsLength is: ", polygonRegionsLength);

     // Tracks each polygon made
     var polygonTrackers = [];
     
     // Construct the polygons.
     for(i=0; i<polygonRegionsLength; i++)
     {    
          // Define the LatLng coordinates for the polygon's path.
          var polygonCoords = polygonRegions[i];

          // Construct the polygon.
          var polygon = new google.maps.Polygon({
               paths: polygonCoords,
               strokeColor: '#000000',
               strokeOpacity: 0.8,
               strokeWeight: 2,
               fillColor: '#FFFFFF',
               fillOpacity: 0.35
          });

          // Keep tracked of whether selected or not
          polygon.highlighted = 0;

          // id for this polygon
          polygon.myId = i;

          polygon.setMap(mapToDrawOn);
          polygonTrackers.push(polygon);
     }

     return  polygonTrackers;
}

